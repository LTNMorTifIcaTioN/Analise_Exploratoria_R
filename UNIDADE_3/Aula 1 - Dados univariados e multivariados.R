# Medidas e Escalas

'
> A análise multivariada se refere a todas as técnicas estatísticas que simultaneamente analisam múltiplas medidas
sobre indivíduos ou objetos sob investigação.
'

# Escalas nominais
'
> Fazem parte de um grupo maior, denominado escalas de medida não-métrica.
> Uma escala nominal designa números para rotular ou identificar indivíduos ou objetos.
> Os números designados aos objetos não tem significado quantitativo.
> Exemplo: 1 - Mulher, 2 - Homem. Esses números não passarão por cálculos.
'

# Escalas intervalares
'
> Fazem parte de um grupo maior chamado escalas de medida métrica.
> As escalas intervalares fornecem o mais alto nível de precisão de medida, permitindo que operações matemáticas sejam
executadas sobre os dados.
> Apresentam unidades constantes de medida.
> Cada uma tem um ponto zero arbitrário diferente, e nenhuma indica um valor nulo.
> Exemplos: Fahrenheit e Celius
'

# Demonstração dos conceitos de causalidade, correlação e interação entre variáveis em conjuntos de dados multivariados
x<-c(1,2,3,4,5) #criação do vetor x

y<-c(6,7,8,9,10) #criação do vetor y

cov(x,y)

cor(x,y)

# Correlação
'
> Refere-se à medida de relação entre duas variáveis.
> Se há medida, há um valor que a define.
> A medida da correlação é dada pelo coeficiente de correlação.
Ex: Índice da bolsa e cotação do dólar.

Os índices de correlação variam de -1 a +1
'

# Causalidade
'
Este conceito inclui uma relação de causa entre dois fenômenos
Ex: De fato, o aumento da temperatura é a causa do aumento na venda de sorvetes. Mas no período do verão as pessoas
tendem a gastar mais. Será o motivo o calor, ou a data festiva?
'