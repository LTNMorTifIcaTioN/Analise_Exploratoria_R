# Aula 1 - Análise Exploratória de Dados: definição e importância

"
Análise Multivariada
> A análise mutivariada diz respeito a usar diversas variáveis em conjunto nas mesmas análises;
> As técnicas multivariadas possuem um poder anaítico nas mãos do pesquisador, porém colocam maior reponsabilidade sobre
ele para garantir que a estrutura estatística e teórica na qual se baseiam também está sustentada.
"

"
Mídias Sociais
> A coleta de dados de redes sociais é uma prática comum de empresas de médio e grande porte;
> Dados só podem ser coletados pela própria empresa via consentimento e anonimato, conforme a LGPD;
> Cada rede social contría sua API como prefere, porém, geralmente se trata de um módulo ou biblioteca em uma ou mais
linguagens de programação que permitem a conexão usando dados dos usuário, que nominalmente está coletando as
informações, no caso geral, a empresa interessada nelas.
"